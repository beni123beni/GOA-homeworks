# 1)გააკეთეთ 10-10 მაგალითი and და or ოპერატორებზე (>, <, ==, >=, <=, !=)
a = 5
b = 10
print(a > 3 and b < 15)  # True
print(a == 5 and b != 10)  # False
print(a >= 5 and b <= 10)  # True
print(a < 3 and b > 8)  # False
print(a != 5 and b == 10)  # False
print(a > 2 and b < 12)  # True
print(a == 5 and b == 10)  # True
print(a <= 5 and b >= 10)  # True
print(a < 5 and b > 10)  # False
print(a != 4 and b != 9)  # True
print(a > 3 or b < 5)  # True
print(a == 4 or b != 10)  # False
print(a >= 5 or b <= 8)  # True
print(a < 3 or b > 8)  # True
print(a != 5 or b == 10)  # True
print(a > 6 or b < 12)  # True
print(a == 5 or b == 9)  # True
print(a <= 4 or b >= 10)  # True
print(a < 5 or b > 10)  # False
print(a != 4 or b != 9)  # True

